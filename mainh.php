  <h2>Sigue el paso a paso</h2>
  <ol>
    <li class="letra">Pon la carne picada en un bol. Puedes usar solo carne de cerdo o carne de ternera, pero para potenciar el sabor de las albóndigas sin gluten te recomendamos una mezcla de ambas (a partes iguales o dando preferencia a una de ellas). Pica los ajos y el perejil y añade estos ingredientes a la carne, así como el huevo, un poco de sal y pimienta. Mezcla todo muy bien y deja reposar una hora o incluso de un día para otro. </li>
     <br> <img class="imagen" src="images/1.jpg" alt="1"> <br>

    <li class="letra">Pasado el tiempo de reposo de la carne, empieza a preparar la salsa. Para ello, corta la cebolla en juliana y pela los dientes de ajo.</li>
    <br> <img class="imagen" src="images/2.jpg" alt="2"><br>
    <li class="letra">Calienta una cazuela con un buen chorro de aceite de oliva, añade la cebolla y deja que se poche. Cuando esté casi pochada, agrega los dientes de ajo enteros.</li>
    <br> <img class="imagen" src="images/3.jpg" alt="3"><br>
    <li class="letra">Vierte el vino blanco, deja que reduzca el alcohol durante unos minutos y añade el agua o el caldo de pollo. Puedes hacer el caldo con una pastilla de consomé de pollo o preparar un buen caldo de pollo casero. En cualquier caso, deja que se cocine la salsa unos 7-8 minutos.</li>
    <br> <img class="imagen" src="images/4.jpg" alt="4"><br>
    <li class="letra">Cogemos porciones de carne y forma las albóndigas con las manos. Lo ideal es hacer bolas del tamaño de una pelota de golf, pero si lo prefieres puedes hacerlas más pequeñas. Luego, pon en un plato la harina de arroz o la maicena y pasa las albóndigas por ella para rebozarlas bien. Para hacer albóndigas sin gluten la harina de arroz es ideal, aunque la maicena e incluso la harina de garbanzos son también buenas opciones.</li>
    <br> <img class="imagen" src="images/5.jpg" alt="5"><br>
    <li class="letra">Calienta una sartén con bastante aceite y fríe las albóndigas. Sácalas y resérvalas sobre papel de cocina absorbente. También puedes cocinar las albóndigas sin gluten al horno a 180 ºC durante 20 minutos.</li>
    <br> <img class="imagen" src="images/6.jpg" alt="6"><br>
    <li class="letra">Saca la salsa y tritúrala. Después, vuelve a ponerla en la cazuela.</li>
    <br> <img class="imagen" src="images/7.jpg" alt="7"><br>
    <li class="letra">Añade las albóndigas y deja que se cocinen en la salsa durante 15 minutos a fuego medio-bajo. Pon un poco de sal y pimienta si lo consideras necesario.</li>
